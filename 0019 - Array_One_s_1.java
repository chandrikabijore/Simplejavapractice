package com.chandrika;

public class Array_One_s_1
{
    public static void main(String[] args)
    {
        int[] a = {10, 20, 30, 40, 50};
        System.out.println("One dimensional arrays are : ");
        for (int i = 0; i < a.length; i++)
        {
            System.out.println("a["+i+"] : " + a[i]);
        }
    }
}
